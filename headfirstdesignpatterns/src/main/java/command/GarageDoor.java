package command;

public class GarageDoor {
    public void up() {
        System.out.println("차고지 열기");
    }

    public void down() {
        System.out.println("차고지 닫기");
    }

    public void stop() {
        System.out.println("차고지 멈춤");
    }

    public void lightOn() {
        System.out.println("차고지 불켜기");
    }

    public void lightOff() {
        System.out.println("차고지 불끄기");
    }

}
